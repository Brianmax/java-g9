package loops;

public class Bucles {
    public static void main(String[] args) {

        for(int i = 0; i < 10; i++) {
            System.out.println("Hola a todos");
        }

        System.out.println("============================");
        int j = 0;
        while (j < 10) {
            System.out.println("Hola a todos");
            j++;
        }
    }
}
